#include<iostream>
using namespace std;
int main()
{
	int a=10;
	
	// preincrement.......
	
	int b=++a;
	cout<<"value of a: "<<b;
	
	// predecrement.......
	
    int c=--a;
	cout<<"\nvalue of a: "<<c;
	
	// postincrement.......
	
	int d=a++;
	cout<<"\nvalue of a: "<<d;
	
	// postdecrement.......
	
	int e=a--;
	cout<<"\nvalue of a: "<<e;
}	
