#include<iostream>
using namespace std;
int main()
{
 int num;
 cout<<"ENTER YOUR NUMBER: ";
 cin>>num;
   switch(num)
  {
     case 1: (num==1);
     
     cout<<"sunday";
      
     break;
    
     case 2: (num==2);
 
     cout<<"monday";
     
     break;
 
     case 3: (num==3);
 
     cout<<"tuesday";
     
     break;
 
     case 4: (num==4);
 
     cout<<"wednesday";
     
     break;
 
     case 5: (num==5);
 
     cout<<"thursday";
     
     break;
 
     case 6: (num==6);
 
     cout<<"friday";
     
     break;
 
     case 7: (num==7);
 
     cout<<"saturday";
     
     break;
 
     default:
 
     cout<<" not in week";
     
    }
 
 return 0;
 
}
