#include<iostream>
using namespace std;
int main()
{
    int a=20;
    int b=10;
    int c;
    c=a+b;
    cout<<"THE ADDITION VALUE OF c:"<<c<<endl;
    
    c=a*b;
    cout<<"THE MULTIPLICATION VALUE OF c:"<<c<<endl;
   
    c=a/b;
    cout<<"THE DIVISION VALUE OF c:"<<c<<endl;
   
    c=a-b;
    cout<<"THE SUBSTRACTION VALUE OF c:"<<c<<endl;
   
    return 0;
}
                       
                       
